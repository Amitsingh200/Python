from turtle import *
title('Draw a Boat using Python')
bgcolor("#6495ED")

#Draw boat
color("#966919","#5C4033")
penup()
goto(60,-150)
begin_fill()
pendown()
forward(200)
left(60)
forward(80)
setheading(180)
forward(280)
left(120)
forward(80)
end_fill()
backward(80)
setheading(0)
forward(140)
left(90)

#Draw the stick 
pensize(9)
forward(125)
backward(5)
penup()

#draw left wing
